<p>
	Užice is a city in Serbia in the west in the mountains. It is built into the
	hilly rural land. It is dense and transitions sharply from city to farmland.
	Farmer's villages connect the larger cities in a loose network of dirt tracks
	and skinny asphalt roads. All in all it is great land for hiking and camping.
</p>

<p>
	Serbians love their countryside but seldem travel between towns on foot or on
	bike. For example you can walk 30 minutes from the center of Belgrade across
	the Panchevo bridge and be in the deep countryside. This hiking trail is not
	used by walkers or runners even on the weekends. To Serbians intercity foot
	travel is a quaint affixation of hippie types. As long as they can easily tell
	that you are not a gypsy you will pass without much attention from people. The
	wild dogs during the daytime are sedate and will not bother you.
</p>

<p>
	The walk starts in the north west side of Užice on the hillside. The first few
	climbs are the steepest ones. Near Užice the taxi cabs will circle you like
	vultures expecting you to give up and drive back in exchange for exorbitant
	amounts of cash. The walk out of the Užice switches between steep farm track
	and asphalt roads. The terrain eventually plateaus into steady rolling hills.
</p>

